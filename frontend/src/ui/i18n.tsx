type TKey = keyof typeof tr;

interface LangCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: TKey) => string;
}

const LangCtx = createContext<LangCtx>({ lang: "tr", setLang: () => {}, t: (k) => k as string });

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem("recipe_ai_lang");
    return saved === "en" ? "en" : "tr";
  });

  function setLang(l: Lang) {
    setLangState(l);
    localStorage.setItem("recipe_ai_lang", l);
  }

  const t = (key: TKey): string => (lang === "en" ? en : tr)[key] as string;

  return <LangCtx.Provider value={{ lang, setLang, t }}>{children}</LangCtx.Provider>;
}

export function useLanguage() {
  return useContext(LangCtx);
}
